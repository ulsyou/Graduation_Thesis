class User {
  final String id;
  final String fullName;
  final String image;
  final String phoneNumber;
  final String email;
  final String expertise;
  final String role;

  User({
    required this.id,
    required this.fullName,
    required this.image,
    required this.phoneNumber,
    required this.email,
    required this.expertise,
    required this.role,
  });
}